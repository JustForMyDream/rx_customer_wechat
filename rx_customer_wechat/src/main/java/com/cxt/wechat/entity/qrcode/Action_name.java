public enum Action_name {
    QR_SCENE("QR_SCENE"), QR_LIMIT_SCENE("QR_LIMIT_SCENE"), QR_LIMIT_STR_SCENE("QR_LIMIT_SCENE");
    private String type;

    private Action_name(String type) {
        this.type = type;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    @Override
    public String toString() {
        return this.type;
    }
}
